// Let's create a function which can add 2 numbers

// 1. Function defination (One time)

function addTwoNumbers(n1, n2) {
    // function body
    // Every function return something
    return n1 + n2;
}

// 2. Function calling/invoking (Many time)
//Here I define the result var to get the return data as above n1 and n2
// Associativity Principle
   // left = right
var result = addTwoNumbers(50, 80); // To get return value

console.log(result);